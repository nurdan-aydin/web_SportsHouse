                                <h3>Tenis'de Gündem</h3>
                                <p>Raket Seçimi Nasıl Olmalı?
                                    Tenise yeni başlayan arkadaşlarımızın en çok merak ettiği konulardan bir tanesi de tenis ürünleri. Hangi raketi almam lazım? Ağırlığı önemli mi? Raket gribi ne demek? Kıyafet ne olmalı? Ayakkabı önemli mi? gibi bir sürü soruyla karşılaşıyoruz. Fazla zamanınızı almadan bunlara cevap verip sizleri en iyi şekilde yönlendirmek isteriz.

Raket konusu oldukça önemli. Doğru raketi uygun fiyata almanız ve uzun vadeli kullanmanızı isteriz. “Raket fiyatları çok yükseldi” dediğinizi duyar gibiyiz,  bu sebeple size güzel bir çözüm sunmak istedik. Eğer tenise yeni başlıyorsanız, hemen gidip bir raket satın almak yerine öncelikle eğitim aldığınız kişi veya akademiden size ilk 4-5 antrenmanınız için raket sağlamasını ve sonrasında da eğitim aldığınız kişiden sizi yönlendirmesini isteyin. Sizi tanıdığı için raketin kaç gram olacağını, grip kalınlığının ne kadar olması gerektiğini en iyi bilen kişi o olacaktır. Başlamadan kendi tecrübenize göre alırsanız hata yapıp raketinizden memnun kalmayabilir, size uygun olmayan rakete gereğinden daha fazla bütçe ayırabilirsiniz.</p>
